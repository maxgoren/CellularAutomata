
public class Point {
    public int x;
    public int y;
    Point(int x, int y) {
        this.x = x;
        this.y = y;
    }
    public int getX() {
        return x;
    }
    public int getY() {
        return y;
    }
    public void setX(int X) {
        this.x = X;
    }
    public void setY(int Y) {
        this.y = Y;
    }
}
