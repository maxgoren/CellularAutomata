
public class Point {
    public int x;
    public int y;
    Point(int _x, int _y) {
        this.x = _x;
        this.y = _y;
    }
    public int getX() {
        return x;
    }
    public int getY() {
        return y;
    }
    public void setX(int _x) {
        this.x = _x;
    }
    public void setY(int _y) {
        this.y = _y;
    } 
}
